const DIFF = lvl => lvl.includes('12+') ? 'Advanced' : lvl.includes('11') ? 'Intermediate' : 'Beginner';
const MIN = lvl => lvl.includes('12+') ? 60 : lvl.includes('11') ? 45 : 30;
const ASSESS = ['Auto-graded quiz', 'Short written interpretation', 'Uploaded notebook', 'Reflection questions'];

const labs = [];
let id = 0;
TRACKS.forEach(t => {
  L[t.key].forEach((row, i) => {
    id++;
    const [subtopic, title, level, skills, output] = row;
    labs.push({
      id: String(id).padStart(3, '0'),
      track: t.name, trackKey: t.key, icon: t.icon, color: t.color,
      subtopic, title, level, skills,
      data: t.data, tools: t.tools, output,
      assessment: ASSESS[i % ASSESS.length],
      difficulty: DIFF(level), minutes: MIN(level),
      steps: t.steps,
    });
  });
});

